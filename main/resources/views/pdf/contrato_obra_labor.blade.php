            Entre <strong>EL EMPLEADOR</strong> y <strong>EL TRABAJADOR</strong>, de las
            condiciones ya dichas, identificados como aparece al pie de sus firmas, se
            ha celebrado el presente
            <strong>CONTRATO DE TRABAJO POR EL TéRMINO DE LA OBRA O LABOR
                CONTRATADA</strong>, regido además por las siguientes
            <strong>CLáUSULAS: </strong>
            <strong><u>PRIMERA. LABOR CONTRATADA:</u></strong> EL EMPLEADOR contrata al
            TRABAJADOR para desempeñarse como se indicó en el recuadro
            inicial, donde deberá ejecutar las labores y actividades asignadas
            por el EMPLEADOR o su representante, las cuales se encuentran estipuladas en
            el recuadro inicial y que también serán puestas de presente al
            TRABAJADOR en desarrollo del presente contrato, donde claramente las
            indicaciones recibidas tendrán relación directa con el cargo
            contratado conforme al MANUAL DE FUNCIONES Anexo N° 1 y parte integral
            del presente documento. <strong><u>SEGUNDA. </u></strong><strong><u>OBLIGACIONES DEL TRABAJADOR:</u></strong> El
            empleador contrata
            los servicios personales del trabajador y éste se obliga:
            <strong>1. </strong>Aceptar y efectuar las labores encomendadas por EL
            EMPLEADOR, que deberán estar a su vez, vinculadas con el cargo o
            labor contratada. <strong>2. </strong>A no comunicar con terceros, salvo
            autorización expresa, las informaciones que tengan sobre su trabajo,
            especialmente sobre los conceptos que sean de naturaleza reservada o cuya
            divulgación pueda ocasionar perjuicios al empleador, lo que no obsta
            para denunciar delitos comunes o violaciones del contrato o de las normas
            legales del trabajo ante las autoridades competentes.
            <strong>3. </strong>Conservar y restituir en buen estado, salvo el deterioro
            natural, los instrumentos y útiles que le hayan sido facilitados y
            las materias primas sobrantes. <strong>4. </strong>Guardar rigurosamente la
            moral en las relaciones con sus superiores y compañeros.
            <strong>5. </strong>Comunicar oportunamente al empleador las observaciones
            que estime conducentes a evitarle daños y perjuicios.
            <strong>6. </strong>Observar las medidas preventivas higiénicas,
            prescritas por el médico del empleador o por las autoridades del
            ramo. <strong>7. </strong>Comunicar a su jefe inmediato cualquier falla
            humana, física o mecánica, que se presente en la
            realización del trabajo, para tomar medidas correctivas.
            <strong>8. </strong>Usar siempre implementos de seguridad que la empresa les
            proporciona, lo mismo que los overoles y zapatos de labor.
            <strong>9. </strong>Desarrollar las gestiones que se le encomienden con
            rapidez y eficiencia.
            <strong><u>PARáGRAFO PRIMERO. ACUERDO DE CONFIDENCIALIDAD</u>. </strong>En concordancia con lo
            establecido en
            el numeral 2 del artículo 58,
            del Código Sustantivo del Trabajo, durante la vigencia del presente
            contrato y de manera indefinida, el empleado se obliga y se compromete a no
            comunicar a terceros, ni usar en su propio provecho, las informaciones de
            naturaleza reservada que tenga o llegue a conocer en razón de si
            vinculación laboral y cuya divulgación pueda ocasionar
            perjuicios a la empresa. Como consecuencia el incumplimiento de lo
            señalado en la presente cláusula se considera FALTA GRAVE y
            faculta al EMPLEADOR a dar por terminado el contrato de trabajo bajo justa
            causa. <strong><u>PARáGRAFO SEGUNDO.</u></strong> De acuerdo al
            numeral 2 de la presente cláusula, las obligaciones derivadas del
            presente convenio, se considerará como una FALTA GRAVE, que
            dará lugar a la terminación del contrato de trabajo o a la
            imposición de las sanciones que libremente determine el empleador, de
            acuerdo con la gravedad de la falta, sin perjuicio del cobro
            económico por los perjuicios ocasionados a los que haya lugar. El
            TRABAJADOR SE OBLIGA: 1. Restituir al EMPLEADOR todos los elementos de
            trabajo, como: la dotación, las herramientas, los documentos y todo
            aquello que la compañía haya entregado al trabajador para el
            buen desarrollo de la labor contratada; 2. Si se llegare a incumplir alguna
            de las obligaciones con posterioridad a la terminación del contrato
            de trabajo, EL EMPLEADOR se reserva el derecho de reclamar por la vía
            judicial o extrajudicial la indemnización de los perjuicios causados,
            para lo cual desde ya se reconoce en el presente contrato; 3. EL TRABAJADOR
            manifiesta que ha sido informado y es consciente que conoce por su trabajo
            información confidencial DEL EMPLEADOR, de sus accionistas,
            demás empleados y relacionados, entre otros.
            <strong><u>PARáGRAFO TERCERO.</u></strong><u><strong>REQUERIMIENTO DE AUTORIDADES</strong></u>. En caso
            que
            alguna autoridad de carácter administrativo o judicial
            requiera la presentación de información que pueda
            caracterizarse como de carácter confidencial o reservado DEL
            EMPLEADOR, es obligación del TRABAJADOR dar traslado oportunamente al
            representante legal del EMPLEADOR y/o a la junta directiva del EMPLEADOR
            sobre el requerimiento, antes de divulgar cualquier información y
            sólo con la autorización de éstos podrá proceder
            a divulgar dicha información.
            <strong><u>TERCERA. PROHIBICIONES DEL TRABAJADOR:</u></strong>Se le prohíbe al trabajador lo siguiente:
            <strong>1.</strong> Manejar máquinas o vehículos distintos de
            aquellas que les han entregado para el desempeño de sus funciones.
            <strong>2.</strong> Levantar o movilizar manualmente cargas que pongan en
            peligro su integridad física, si éstas no se ejecutan de
            acuerdo con las instrucciones de seguridad sobre la posición del
            cuerpo de los pies y de los brazos en la manipulación de cargas.
            <strong>3.</strong> Consumir alimentos o bebidas durante la
            realización de la gestión a su cargo.<strong> 4.</strong>
            Consumir y sustraer del área de trabajo o establecimiento, los
            útiles de trabajo y las materias primas o productos, sin permiso del
            empleador. <strong>5. </strong>Es absolutamente prohibido y se considera
            falta grave presentarse al trabajo en estado de embriaguez o bajo la
            influencia de narcóticos o drogas alucinógenas.
            <strong>6.</strong> Conservar armas de cualquier clase en el sitio del
            trabajo, a excepción de las que con autorización legal puedan
            llevar los celadores<strong>. 7.</strong> Faltar al trabajo sin justa causa
            de impedimento o sin permiso del empleador, excepto en los casos de huelga,
            en los cuales deben abandonar el lugar del trabajo.
            <strong>8.</strong> Hacer colectas, rifas y suscripciones o cualquier clase
            de propaganda en los lugares de trabajo. <strong>9.</strong> Usar los
            útiles o herramientas suministradas por el empleador en objetos
            distintos del trabajo contratado<strong>. 10. </strong>Desarrollar
            cuestiones ajenas a las encomendadas.
            <strong><u>PARáGRAFO PRIMERO.</u></strong> Se considera hecho grave
            para EL TRABAJADOR el haber tenido conocimiento o participación por
            acción, omisión, negligencia o complicidad, en comportamiento
            o hechos delictivos o en contra de la moral, que perjudiquen los intereses
            del empleador o del usuario, sus funcionarios, clientes o allegados, o el
            hecho de tener conocimiento de ellos y no informar oportunamente al
            empleador, sus representantes o al usuario, o dar informaciones falsas o
            extemporáneas sobre los mismos.
            <strong><u>CUARTA. DURACIóN DEL CONTRATO.</u></strong> El presente
            contrato se celebra por el tiempo que dure la realización de la obra
            o labor contratada, hasta su terminación total o parcial.
            <strong><u>QUINTA. PERIODO DE PRUEBA:</u></strong>Acuerdan las partes fijar como periodo de prueba el
            término
            de
            DIECIOCHO (18) días, de conformidad con lo que establece la ley para
            el efecto, o en su defecto, la quinta parte de lo que se estime para la
            concreción de la obra o labor contratada.
            <strong><u>SEXTA. JUSTAS CAUSAS PARA DAR POR TERMINADO LA RELACION CONTRACTUAL:</u></strong>
            Son justas causas para dar por terminado unilateralmente el presente
            contrato por cualquiera de las partes, como el incumplimiento de las
            obligaciones y prohibiciones que se expresan en los artículos 57 y
            siguientes del código sustantivo del trabajo. Además, el
            incumplimiento o violación de las normas establecidas en este
            contrato, en el reglamento interno de trabajo, en el programa de salud
            ocupacional y los reglamentos de higiene y seguridad, además las
            previamente establecidas por el empleador o sus representantes y en especial
            las siguientes:
            <strong>1.</strong> Paralizar temporal y permanentemente la labor para la
            cual ha sido contratado y el cual se considerará falta grave y justa
            causa para terminar el vínculo contractual. <strong>2.</strong> La
            violación por parte del trabajador de cualquiera de sus obligaciones,
            deberes y prohibiciones legales, contractuales o reglamentarias, aún
            por la primera vez. <strong>3.</strong> La no asistencia puntual al trabajo,
            sin excusa suficiente a juicio del empleador, aún por la primera vez.
            <strong>4. </strong>La ejecución por parte del trabajador de labores
            remuneradas a servicio de terceros sin autorización del empleador,
            aún por la primera vez. <strong>5. </strong>La revelación de
            secretos y datos reservados de la empresa, aún por la primera vez.
            <strong>6. </strong>Las desavenencias con sus compañeros de trabajo,
            aún por la primera vez. <strong>7. </strong>El hecho de que EL
            TRABAJADOR llegue embriagado o en estado de alicoramiento al trabajo o que
            ingiera bebidas embriagantes en el sitio de trabajo, aun por la primera vez.
            <strong>8. </strong>El hecho que EL TRABAJADOR suspenda la labor sin
            autorización, aún por la primera vez ó abandone el
            sitio de trabajo o el lugar donde deba cumplir con sus funciones sin el
            permiso de sus superiores, aún por la primera vez.
            <strong>9. </strong>La no asistencia a una sesión completa de la
            jornada de trabajo, o más, sin excusa suficiente a juicio del
            empleador, salvo fuerza mayor o caso fortuito, aún por la primera
            vez. <strong>10. </strong>No aceptar el cambio de turno o lugar de trabajo
            dentro y fuera de la empresa sin razones válidas expuestas por el
            empleador, aún por la primera vez. <strong>11. </strong>Amenazar a
            sus compañeros de trabajo, superiores y/o clientes de la empresa,
            aún por la primera vez. <strong>12. </strong>El usar, vender,
            distribuir y/o portar drogas alucinógenas o estupefacientes en el
            sitio de trabajo, aún por la primera vez. <strong>13. </strong>El
            utilizar el nombre de la empresa para obtener cualquier tipo de provecho
            para sí, para sus parientes o allegados, aún por la primera
            vez. <strong>14. </strong>El calumniar, injuriar, pelear e irrespetar de
            palabra o de obra, dentro o fuera del sitio de trabajo con sus
            compañeros de trabajo, superiores o familiares de éste,
            aún por la primera vez. <strong>14. </strong>Cualquier
            información falsa suministrada a la empresa, aún por la
            primera vez. <strong>15. </strong>El sustraer implementos de trabajo o
            insumos que se utilizan en la empresa sin la autorización previa y
            escrita, aún por la primera vez. <strong>16. </strong>No presentar
            los reportes de trabajo en el momento indicado por la empresa, aún
            por la primera vez. <strong>17. </strong>No cumplir con la labor programada,
            aún por la primera vez. <strong>18. </strong>Entregar
            información o documentación incompleta o retardada, aún
            por la primera vez. <strong>19. </strong>Deteriorar, perder o dañar
            los activos asignados para el desarrollo de sus labores, aún por la
            primera vez. <strong>20. </strong>Detener un trabajo por falta de
            información y no comunicarlo. <strong>21. </strong>El hacer caso
            omiso a las llamadas de atención por ineficiencia en la labor,
            aún por la primera vez. <strong>22. </strong>Entorpecer las tareas de
            sus compañeros, aún por la primera vez.
            <strong>23. </strong>Extraer información de la empresa (clientes,
            productos, trabajos etc.) para ser utilizada en provecho personal o de la
            competencia, aún por la primera vez. <strong>24. </strong>El realizar
            trabajos para provecho o uso personal sin autorización de la empresa,
            aún por la primera vez. <strong>25. </strong>Desacatar o menos
            preciar las sugerencias ofrecidas por sus superiores.
            <strong>26. </strong>Realizar rifas o negocios personales dentro de la
            empresa, aún por la primera vez. <strong>27. </strong>La
            pérdida de herramientas o implementos de trabajo dados a su cargo y
            bajo su responsabilidad y cuidado, aún por la primera vez.
            <strong>28. </strong>Cualquier violación de las obligaciones, deberes
            y prohibiciones pactadas en este contrato, así como también de
            los señalados en la cláusula especial de este documento,
            aún por la primera. <strong>29. </strong>La terminación total
            o parcial del contrato de servicios personales o especializados por parte
            del cliente o usuario del EMPLEADOR, el cual terminara inmediatamente la
            obra o la labor determinada para la cual ha sido contratado El TRABAJADOR,
            entendiéndose que el objeto y el fin del contrato suscrito por el
            TRABAJADOR Y EL EMPLEADOR ha dejado de existir en la vida jurídica.
            <strong><u>PARáGRAFO PRIMERO. INDEMNIZACION DE PERJUICIOS</u></strong>. En caso que EL TRABAJADOR incurra
            en
            cualquier conducta descrita en el
            presente contrato, como el incumplimiento de las obligaciones y
            prohibiciones que se expresan en los artículos 57 y siguientes del
            código sustantivo del trabajo, además el incumplimiento o
            violación de las normas establecidas en el reglamento interno de
            trabajo, higiene y seguridad y las previamente establecidas por el empleador
            o sus representantes y en el especial lo consagrado en el numeral 1 y 2 de
            la presente clausula y que de esta parálisis temporal o permanente de
            la labor para cual ha sido contratado, genere daños y perjuicios a
            los clientes, usuarios, socios, accionistas o representantes de la
            compañía, El EMPLEADOR tendrá derecho a exigir al
            TRABAJADOR el pago de indemnización por perjuicios, daño
            emergente y lucro cesante, de acuerdo con el Artículo 1600 del
            Código Civil Colombiano.
            <strong><u>PARáGRAFO SEGUNDO.</u></strong><strong><u>LIQUIDACIóN Y ENTREGA DE ACREENCIAS
                    LABORALES.</u></strong>
            En consideración a que toda liquidación y pago de salarios y
            prestaciones sociales exige varios días para obtener los datos e
            informes necesarios y así mismo, implica su revisión,
            realizar, aprobar la liquidación definitiva constituir la partida de
            pago correspondiente. Las partes de común acuerdo, aclaran que existe
            un tiempo prudencial y razonable para estos efectos, tal como en su
            oportunidad lo manifiesto la Corte Suprema de Justicia. Dentro de este plazo
            prudencial y de buena fe EL EMPLEADOR podrá liquidar y pagar los
            salarios y/o prestaciones sociales sin que por ello incurra en mora de
            ninguna naturaleza ni quede obligado a pagar la indemnización
            moratoria contemplada de la ley 789 de 2002. De igual manera se deja expresa
            constancia de la especial obligación del TRABAJADOR de tramitar
            directamente su paz y salvo empresarial, documento sin el cual no se pueden
            tramitar las acreencias laborales que le correspondan y después del
            tiempo prudencial del caso, se procederá a la consignación en
            el banco Agrario de Colombia en una cuenta de depósito judicial.
            <strong><u>SEPTIMA.- NUEVA OBRA O CAMBIO DEL TERMINO DEL CONTRATO</u>: </strong>Si al finalizar la obra o la
            labor
            contratada, El EMPLEADOR desea continuar
            con El TRABAJADOR en otra obra o labor distinta a la aquí contratada,
            EL EMPLEADOR y TRABAJADOR darán por terminado por mutuo acuerdo el
            presente contrato, obligándose el empleador a cancelar al trabajador,
            lo correspondiente a la liquidación de prestaciones sociales.
            Después de realizado lo anterior, el empleador y el trabajador
            procederán a realizar un nuevo contrato por el termino de
            duración según lo pacten las partes.
            <strong><u>OCTAVA. ESIPULACION SALARIAL:</u></strong> EL EMPLEADOR
            cancelará al TRABAJADOR por concepto de salario, lo estipulado en el
            recuadro inicial. Los tiempos de pago del salario serán los
            establecidos en el recuadro inicial y se realizará mediante
            transacción electrónica, para lo cual EL TRABAJADOR debe hacer
            apertura de cuenta de nómina una vez se firme el presente contrato, o
            en caso que no sea posible lo relativa a la cuenta bancaria,
            excepcionalmente se pagará directamente en efectivo.
            <strong><u>NOVENA. LUGAR DE PRESTACIóN DEL SERVICIO.</u></strong>
            EL TRABAJADOR desarrollará sus funciones en el Municipio de
            Girón (Santander); sin perjuicio de que el EMPLEADOR requiera del
            traslado del TRABAJADOR para lo cual, éste último acepta con
            la suscripción del presente contrato, cualquier cambio de lugar de
            trabajo siempre y cuando no desmejore sus condiciones de vida.
            <strong><u>DECIMA.- MODIFICACIONES.</u></strong> El presente contrato
            remplaza en su integridad y deja sin efecto alguno cualquiera otro contrato
            verbal o escrito celebrado entre las partes con anterioridad. Las
            modificaciones que se acuerden al presente contrato se anotarán a
            continuación de su texto.
            <strong><u>DECIMA PRIMERA.- CLAUSULA DE EXCLUSIVIDAD:</u></strong> durante
            la vigencia de este acuerdo, EL TRABAJADOR se compromete formalmente a
            trabajar exclusivamente para EL EMPLEADOR y no puede ejercer ninguna otra
            función en cualquier otra empresa a título que sea en el caso
            de cese por los motivos que fueran, se compromete a no ofrecer sus servicios
            directa o indirectamente a cualquier empresa, o cliente, para realizar la
            misma actividad que desempeña con el cliente. Entre EL TRABAJADOR y
            EL EMPLEADOR existe exclusividad laboral. El EMPLEADOR es el único
            responsable del pago de salarios, prestaciones sociales, indemnizaciones, y
            demás emolumentos a los cuales tenga derecho el trabajador.
            <strong><u>DéCIMA SEGUNDA</u>: </strong>El TRABAJADOR mediante este
            contrato declara que ha leído, entiende y acepta el contenido de este
            contrato, así como también el del reglamento interno de
            trabajo vigente en la empresa. <br><br>
